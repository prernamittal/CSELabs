		EXPORT Reset_Handler
Reset_Handler
		LDR R0, =SRC
		LDR R1,[R0]
		LDR R2, =DST
		MOV R3, #1
		MOV R4, #0XA
		MOV R5, #0
		MOV R6, #0XF
LOOP	AND R7,R1,R6
		MLA R5,R7,R3,R5
		MUL R3,R4
		LSR R1,#4
		CMP R1,#0
		BNE LOOP
		STR R5, [R2]
STOP 
		B STOP
SRC DCD 0x612
		AREA MYDATA, DATA, READWRITE
DST DCD 0
		END